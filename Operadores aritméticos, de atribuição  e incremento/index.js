/*
Aritméticos
** = Potenciação
* = Multiplicação
/ = Divisão
% = Resto da divisão
+ = Adição / Concatenação
- = Subtração
*/
const numString = "5";
const num1 = 5;
const num2 = 2;
const num3 = 10;
console.log(numString + num1);
console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 ** num2);
console.log(num1 % num2);

console.log((num1 + num2) * num3);
